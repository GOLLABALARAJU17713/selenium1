public class evennum1 {
    public static void main(String[] args)
    {
        int a=3;
        int b=5;
        while(a<10)
        {
            if(a%2==0)
            {
                System.out.println(a);
            }
            a++;
        }
        if (b%2==0)
        {
            System.out.println("this is a even number ");
        }
        else {
            System.out.println("this is odd number");
        }}
}
