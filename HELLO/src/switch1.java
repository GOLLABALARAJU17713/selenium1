public class switch1 {
    public static void main(String []args)
    {
        int num=4;
        switch(num)
        {
            case 1: System.out.println("new idea ok");
                    break;
            case 4: System.out.println("not satisfied");
                    break;
            case 6: System.out.println("satisfied");
                    break;
            case 7: System.out.println("not ok");
                    break;
            case 9: System.out.println("new idea ok");
                    break;
            default: System.out.println("new idea ok");
                    break;
        }
    }
}
